    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        // TODO add your handling code here:
        String id = txtMaterialId.getText();
        String name = txtMaterialName.getText();
        String description = txtDescription.getText();
        String quantity = txtQuantity.getText();
        String price = txtPrice.getText();
        
        if(validateFields()){
            JOptionPane.showMessageDialog(null, "Enter all the fields");
        }
        else{
            try{
                Connection con = ConnectionProvider.getCon();
                PreparedStatement ps = con.prepareStatement("insert into material (Material_id,Material_name,Description,Quantity,Price,MVendor_id, ProductID) values(?,?,?,?,?,?,?);");
                ps.setString(1,id);
                ps.setString(2,name);
                ps.setString(3,description);
                ps.setString(4,quantity);
                ps.setString(5,price);
                ps.setString(6, vendorId);
                ps.setString(7, null);
                ps.execute();
                JOptionPane.showMessageDialog(null, "Material added Successfully");
                setVisible(false);
                new AddMaterial().setVisible(true);
        }
            catch(Exception e){
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }//GEN-LAST:event_saveBtnActionPerformed

