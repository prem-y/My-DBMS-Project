    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        // TODO add your handling code here:
        txtMaterialId.setEditable(false);
        txtVendorId.setEditable(false);
        txtVendorName.setEditable(false); 
        DefaultTableModel model = (DefaultTableModel) materialTable.getModel();
        DefaultTableModel vendorModel = (DefaultTableModel) vendorTable.getModel();
        
        try{
            Connection con = ConnectionProvider.getCon();
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select * from material;");
            while(rs.next()){
                model.addRow(new Object[]{rs.getString("Material_id"), rs.getString("Material_name"), rs.getString("Description"), rs.getString("Quantity"), rs.getString("Price"), rs.getString("MVendor_id"),rs.getString("ProductID")});
            }
            
            rs = st.executeQuery("select * from vendor;");
            while(rs.next()){
                vendorModel.addRow(new Object[]{rs.getString("Vendor_id"), rs.getString("Vendor_name"), rs.getString("Phone_number")});
            }
            
            int num=0;
            rs = st.executeQuery("select count(*) as num from material;");
            while(rs.next()){
                num = Integer.parseInt(rs.getString("num"));
            }
            num+=1;
            txtMaterialId.setText("TBM"+num);
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e); 
        }
    }//GEN-LAST:event_formComponentShown

