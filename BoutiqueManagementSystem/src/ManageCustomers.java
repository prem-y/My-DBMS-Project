    private void updateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateBtnActionPerformed
        // TODO add your handling code here:
        String id = txtCustomerId.getText();
        String name = txtCustomerName.getText();
        String country = txtCountry.getText();
        String state = txtState.getText();
        String city = txtCity.getText();
        String phone =txtPhoneNumber.getText();
        String gender = (String) txtGender.getSelectedItem();
        int age = Integer.parseInt(txtAge.getText());
        
        if(validateFields()){
            JOptionPane.showMessageDialog(null, "Enter all the fields Correctly");
        }
        else{
            try{
                Connection con = ConnectionProvider.getCon();
                PreparedStatement ps = con.prepareStatement("update customer set Customer_id=? , Customer_name=? , "
                        + "Country=? , State=? , City=? , Phone_number=? , Gender=? , Age=? where Customer_id=?");
                ps.setString(1,id);
                ps.setString(2,name);
                ps.setString(3,country);
                ps.setString(4,state);
                ps.setString(5,city);
                ps.setString(6,phone);
                ps.setString(7,gender);
                ps.setInt(8,age);
                ps.setString(9,id);
                ps.execute();
                JOptionPane.showMessageDialog(null, "Customer updated Successfully");
                setVisible(false);
                new ManageCustomers().setVisible(true);
        }
            catch(Exception e){
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }//GEN-LAST:event_updateBtnActionPerformed

    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        // TODO add your handling code here:
        txtCustomerId.setEditable(false);
        DefaultTableModel model = (DefaultTableModel) customerTable.getModel();
        try {
            Connection con = ConnectionProvider.getCon();
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select * from customer");
            while (rs.next()) {
                model.addRow(new Object[]{rs.getString("Customer_id"), rs.getString("Customer_name"), rs.getString("Gender"), rs.getString("Age"), 
                    rs.getString("Phone_number"), rs.getString("City"), rs.getString("State"), rs.getString("Country")});
            }
            
            rs = st.executeQuery("select count(*) as num from customer");
            int num = 0;
            while (rs.next()){
                num = Integer.parseInt(rs.getString("num"));
            }
            num+=1;
            txtCustomerId.setText("TBC"+num);
            updateBtn.setEnabled(false);
            deleteBtn.setEnabled(false);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_formComponentShown

