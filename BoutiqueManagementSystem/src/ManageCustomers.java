    private void customerTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_customerTableMouseClicked
        // TODO add your handling code here:
        int index = customerTable.getSelectedRow();
        TableModel model = customerTable.getModel();
        String id = model.getValueAt(index, 0).toString();
        customerId = id;
        txtCustomerId.setText(id);
        String name = model.getValueAt(index, 1).toString();
        txtCustomerName.setText(name);
        String country = model.getValueAt(index, 7).toString();
        txtCountry.setText(country);
        String state = model.getValueAt(index, 6).toString();
        txtState.setText(state);
        String city = model.getValueAt(index, 5).toString();
        txtCity.setText(city);
        String phone = model.getValueAt(index, 4).toString();
        txtPhoneNumber.setText(phone);
        String gender = model.getValueAt(index, 2).toString();
        txtGender.removeAllItems();
        if(gender.equals("Male")){
            txtGender.addItem("Male");
            txtGender.addItem("Female");
        }
        else{
            txtGender.addItem("Female");
            txtGender.addItem("Male");
        }
        String age = model.getValueAt(index, 3).toString();
        txtAge.setText(age);
        
        saveBtn.setEnabled(false); 
        updateBtn.setEnabled(true);
        deleteBtn.setEnabled(true); 
        txtCustomerId.setEnabled(false); 
    }//GEN-LAST:event_customerTableMouseClicked

